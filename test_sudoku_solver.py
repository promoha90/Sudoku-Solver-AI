from sudoku_solver import Board

def test_board_str():
    # Testing the string representation of the board
    board = Board([
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9]
    ])
    
    expected_output = (
        '+-----------+-----------+-----------+\n'
        '| 5 | 3 | * | * | 7 | * | * | * | * |\n'
        '+-----------+-----------+-----------+\n'
        '| 6 | * | * | 1 | 9 | 5 | * | * | * |\n'
        '+-----------+-----------+-----------+\n'
        '| * | 9 | 8 | * | * | * | * | 6 | * |\n'
        '+-----------+-----------+-----------+\n'
        '| 8 | * | * | * | 6 | * | * | * | 3 |\n'
        '+-----------+-----------+-----------+\n'
        '| 4 | * | * | 8 | * | 3 | * | * | 1 |\n'
        '+-----------+-----------+-----------+\n'
        '| 7 | * | * | * | 2 | * | * | * | 6 |\n'
        '+-----------+-----------+-----------+\n'
        '| * | 6 | * | * | * | * | 2 | 8 | * |\n'
        '+-----------+-----------+-----------+\n'
        '| * | * | * | 4 | 1 | 9 | * | * | 5 |\n'
        '+-----------+-----------+-----------+\n'
        '| * | * | * | * | 8 | * | * | 7 | 9 |\n'
        '+-----------+-----------+-----------+\n'
    )
    
    assert str(board) == expected_output

